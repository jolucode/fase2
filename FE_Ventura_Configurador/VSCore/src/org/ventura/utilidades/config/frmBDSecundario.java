    private void btnAdjuntoCertificado3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdjuntoCertificado3ActionPerformed
        String dirAdjuntos = txtRutaCertificado.getText();
        JFileChooser fc = new JFileChooser(dirAdjuntos);
        fc.setFileSelectionMode(JFileChooser.FILES_ONLY);
        fc.setMultiSelectionEnabled(false);
        int ret = fc.showDialog(this, "Seleccionar");
        if (ret == JFileChooser.APPROVE_OPTION) {
            txtRutaCertificado.setText(fc.getSelectedFile().getAbsolutePath());
        }
    }//GEN-LAST:event_btnAdjuntoCertificado3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        BdsMaestras obj = new BdsMaestras();
        //Sociedad
        obj.setBDId("0");
        obj.setbDServidor(this.txtBDNombreServidor.getText());
        obj.setBDNombre(this.txtBaseDatos.getText());
        obj.setBDUsuario(this.txtBDUsuario.getText());
        obj.setBDClave(this.txtBDPassword.getText());

        obj.setTipoERP(String.valueOf(((TipoConector) cmbTipoConector.getSelectedItem()).getValor()));
        if(this.jTxfRutaArchivos.isEnabled())
            obj.setRutaArchivos(this.jTxfRutaArchivos.getText() == "" ? "-" : this.jTxfRutaArchivos.getText());
        else
            obj.setRutaArchivos("-");
        //SAP
        obj.setServidorLicencia(this.txtSAPServer.getText());
        obj.setERPUsuario(this.txtSAPUsuario.getText());
        obj.setERPClave(new String(this.txtSAPPassword.getPassword()));
        obj.setRucSociedad(this.txtRucSociedad1.getText());
        obj.setTipoServidor(String.valueOf(((TipoServidor) cmbBDTipoServidor.getSelectedItem()).getValor()));
        //Certificado
        obj.setRutaCD(this.txtRutaCertificado.getText());
        obj.setPasswordCD(this.txtClaveCertificado.getText());
        obj.setLogoSociedad(this.txtLogoSociedad.getText());
        //Datos de Envio SUNAT / OSE
        obj.setUsuarioSec(this.txtUserSunat.getText());
        obj.setPasswordSec(this.txtPassSunat.getText());

        //Publicador
        obj.setUsuarioWebService(this.txtUserWS.getText());
        obj.setPasswordWebService(this.txtPassWS.getText());
        obj.setWebservicePublicacion(this.txtPathWS.getText());
        obj.setTipoIntegracionSunat(this.cmbTipoIntegracionWS.getSelectedItem().toString());
        obj.setTipoIntegracionGuias(this.cmbTipoIntegracionGuias.getSelectedItem().toString());
        obj.setUsuarioGuias(this.txtUserGuias.getText());
        obj.setPasswordGuias(new String(this.txtPassGuias.getPassword()));
        obj.setClientID(this.txtClientID.getText());
        obj.setSecretID(new String(this.txtSecretID.getPassword()));
        obj.setScope(this.txtScope.getText());
        this.setBdsMaestras(obj);

        this.hide();

    }//GEN-LAST:event_jButton1ActionPerformed

