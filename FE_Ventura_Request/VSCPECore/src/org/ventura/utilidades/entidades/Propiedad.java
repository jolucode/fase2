package org.ventura.utilidades.entidades;

/**
 * @author VSUser
 */
public class Propiedad {

    String pdescripcion;

    boolean pencriptado;

    String pid;

    public String getPdescripcion() {
        return pdescripcion;
    }

    public void setPdescripcion(String pdescripcion) {
        this.pdescripcion = pdescripcion;
    }

    public boolean isPencriptado() {
        return pencriptado;
    }

    public void setPencriptado(boolean pencriptado) {
        this.pencriptado = pencriptado;
    }

    public String getPid() {
        return pid;
    }

    public void setPid(String pid) {
        this.pid = pid;
    }


}
