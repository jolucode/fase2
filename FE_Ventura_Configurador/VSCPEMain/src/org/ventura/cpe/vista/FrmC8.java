     public static void ModalDynamicPDFViewer() {
        System.out.println(System.getProperty("user.dir"));
        String filePath = System.getProperty("user.dir") + File.separator + "Archivos" + File.separator + "Catalogo_8.pdf";
        //String filePath="C:\\FE_Ventura\\file\\Facturacion Electronica Fase II - Manual de Configuracion y Administracion.pdf";
        System.out.println(filePath);
        File f = new File(filePath);
        if (!f.exists()) {
            JOptionPane.showMessageDialog(null, "No se encontro la ruta", "ERROR", 0);

        } else {

            SwingController controller = new SwingController();
            SwingViewBuilder factory = new SwingViewBuilder(controller);
            JPanel viewerComponentPanel = factory.buildViewerPanel();
            ComponentKeyBinding.install(controller, viewerComponentPanel);
            controller.getDocumentViewController().setAnnotationCallback(
                    new org.icepdf.ri.common.MyAnnotationCallback(
                            controller.getDocumentViewController()));

            FrmC8 window = FrmC8.getInstance();
            window.getContentPane().add(viewerComponentPanel);
            window.pack();
            window.setVisible(true);
            window.setTitle("Facturaci贸n Electr贸nica :: VSCPE");

            controller.openDocument(filePath);

        }

    }
    
    
